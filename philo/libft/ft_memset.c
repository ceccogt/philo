#include <stddef.h>

void	*ft_memset(void *s, int c, size_t n)
{
	int		i;
	char	*str;

	str = (char *) s;
	i = 0;
	while (n > 0)
	{
		str[i] = c;
		i++;
		n--;
	}
	return (str);
}
